const numericRanks = {
    'A': 14,
    'K': 13,
    'Q': 12,
    'J': 11,
    'T': 10,
    '9': 9,
    '8': 8,
    '7': 7,
    '6': 6,
    '5': 5,
    '4': 4,
    '3': 3,
    '2': 2
};

function toHandRepresentation(hand) {
    const card1rank = hand[0][0];
    const card1suit = hand[0][1];
    const card2rank = hand[1][0];
    const card2suit = hand[1][1];

    const repSuit = (card1suit === card2suit) ? 's' : 'o';
    const ranks = [card1rank, card2rank];
    ranks.sort((a, b) => (numericRanks[b] - numericRanks[a]));

    return `${ranks.join('')}${repSuit}`;
}

function shouldJam(seats, community, bb = 4) {
    // TODO: multiple shoves before you
    const myPos = seats.findIndex(seat => seat.isMe);
    const mySeat = seats[myPos];
    const myHand = mySeat.cards;
    const myHandRep = toHandRepresentation(myHand);
    const firstJamPos = (() => {
        for (let i = 0; i < myPos; ++i) {
            if (seats[i].stack === 0) {
                return i;
            }
        }
        return -1;
    })();

    const playersBehindMe = seats.length - myPos - 1;
    const myBbs = Math.min(16, Math.max(1, Math.round((mySeat.stack + mySeat.pot) / bb)));
    const otherBbs = (() => {
        let bbs = 1;
        for (let i = 0; i < seats.length; ++i) {
            if (i !== myPos) {
                const seat = seats[i];
                if (!seat.isFolded) {
                    bbs = Math.max(bbs, Math.round((seat.stack + seat.pot) / bb));
                }
            }
        }
        return bbs;
    })();
    const effectiveBbs = Math.min(myBbs, otherBbs);

    const tableName = (() => {
        if (firstJamPos === -1) {
            switch (playersBehindMe) {
                case 1: return `T_0_1_${effectiveBbs}bb`;
                case 2: return `T_0_2_${effectiveBbs}bb`;
                case 3: return `T_0_3_${effectiveBbs}bb`;
                default: throw new Error(`Invalid number of players behind me: ${playersBehindMe}`);
            }
        } else {
            const firstJamDistance = myPos - firstJamPos;
            switch (firstJamDistance) {
                case 1:
                    switch (playersBehindMe) {
                        case 0: return `T_1_0_${effectiveBbs}bb`;
                        case 1: return `T_1_1_${effectiveBbs}bb`;
                        case 2: return `T_1_2_${effectiveBbs}bb`;
                        default: throw new Error(`Invalid number of players behind me: ${playersBehindMe}`);
                    }

                case 2:
                    switch (playersBehindMe) {
                        case 0: return `T_10_0_${effectiveBbs}bb`;
                        case 1: return `T_10_1_${effectiveBbs}bb`;
                        default: throw new Error(`Invalid number of players behind me: ${playersBehindMe}`);
                    }

                case 3:
                    switch (playersBehindMe) {
                        case 0: return `T_100_0_${effectiveBbs}bb`;
                        default: throw new Error(`Invalid number of players behind me: ${playersBehindMe}`);
                    }

                default: throw new Error(`Invalid first jammer distance: ${firstJamDistance}`);
            }
        }
    })();
    const table = eval(tableName);

    return table.includes(myHandRep);
}

